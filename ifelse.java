import java.util.*;

public class ifelse {
    public static void main(String args[]) {

        try (Scanner sc = new Scanner(System.in)) {
            int x = sc.nextInt();

            if (x % 2 == 0) {

                System.out.println("THIS IS EVEN  one - An AVENGER");

            } else {
                System.out.println("THIS IS ODD one - NOT A AVENGER");

            }
        }
    }
}
